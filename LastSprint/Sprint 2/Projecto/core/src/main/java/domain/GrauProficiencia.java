package domain;

/**
 * Current class is an Enum class that gives us the options available for the proficiency degrees (as GrauProficiencia)
 * that a technical competence (Competencia Tecnica) can have.
 */
public enum GrauProficiencia {
    /**
     * Low (BAIXO) proficiency, is the lowest degree possible.
     */
    BAIXO,
    /**
     * Medium (INTERMEDIO) proficiency.
     */
    INTERMEDIO,
    /**
     * High (ALTO) proficiency.
     */
    ALTO,
    /**
     * Advanced (AVANCADO) proficiency.
     */
    AVANCADO,
    /**
     * God level (GOD_LEVEL) proficiency, the highest degree possible.
     */
    GOD_LEVEL}
